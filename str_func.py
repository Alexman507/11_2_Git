def upper(x):
    """a function that accepts a string as input and returns it with all capital letters
    добавлю строку, так как не знал, что её надо было в девелопе добавлять =D"""
    return x.upper()


def capitalize(x):
    """Эта функция принимает строку и возвращает его в виде строки с первой заглавной буквы"""
    return str.capitalize(x)
